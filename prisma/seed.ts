async function main() {
  console.log("ðŸŒ± Starting seed...");

  // Clear existing skills and categories
  console.log("ðŸ—‘ï¸  Clearing existing data...");
  await prisma.skill.deleteMany();
  await prisma.skillCategory.deleteMany();

  // Seed skill categories and skills
  console.log("ðŸ“¦ Seeding skill categories and skills...");

  for (const category of skillCategories) {
    const { skills, ...categoryData } = category;

    const createdCategory = await prisma.skillCategory.create({
      data: categoryData,
    });

    console.log(`âœ… Created category: ${createdCategory.name}`);

    // Create skills for this category
    const skillsData = skills.map((skillName) => ({
      name: skillName,
      categoryId: createdCategory.id,
    }));

    await prisma.skill.createMany({
      data: skillsData,
    });

    console.log(`   âœ… Added ${skills.length} skills`);
  }

  // Get total counts
  const categoryCount = await prisma.skillCategory.count();
  const skillCount = await prisma.skill.count();

  console.log("\nâœ¨ Seed completed successfully!");
  console.log(`ðŸ“Š Total categories: ${categoryCount}`);
  console.log(`ðŸ“Š Total skills: ${skillCount}`);
}

main()
  .catch((e) => {
    console.error("âŒ Error during seed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
